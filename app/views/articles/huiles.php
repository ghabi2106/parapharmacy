                                        <div class="wpb_wrapper">
                                            <p>
                                                <span style="font-weight: 400;">
                                                Une huile est un corps gras qui à  température ambiante est 
                                                à l’état de liquide et qui ne se mélange pas à l’eau. 
                                                Ce liquide gras et visqueux peut avoir une origine animale, 
                                                végétale, minérale ou synthétique. Plus glissante qu’un lait 
                                                ou une crème, elle invite au massage et à la détente. 
                                                Elle peut s’utiliser sur tout le corps y comprit sur les 
                                                cheveux et le visage, elle va véritablement nourrir et 
                                                illuminer la peau en un geste express.
                                                </span>
                                            </p>
                                            <p>&nbsp;</p>
                                            <h2><b>Pourquoi a-t-elle un intérêt en cosmétique?</b></h2>
                                            <p>
                                                    <span style="font-weight: 400;">
                                                    Si elle n’est pas associée à l’eau, elle ne nécessite pas d’ajout de conservateur 
                                                    dans les formules, car les bactéries se développent principalement en présence d’eau. 
                                                    Par contre, elle requiert l’emploi d’antioxydant comme la vitamine E ou C afin 
                                                    de ralentir son oxydation qui provoquera un rancissement avec le temps.
                                                    </span>
                                            </p>
                                            <p>&nbsp;</p>
                                            <h2><b>Préférez les huiles végétales</b></h2>
                                            <p>
                                                <span style="font-weight: 400;">
                                                    Issues des plantes et donc d’origine naturelle, elles offrent à la peau des bons acides gras 
                                                    polyinsaturés (oméga  3, 6 ou 9), des vitamines, des nutriments et des antioxydants. 
                                                    Elles aident véritablement la peau à se nourrir, se régénérer et se soigner.
                                                </span>
                                            </p>
                                            <p>
                                                <span style="font-weight: 400;">
                                                    Voici quelques huiles très nutritives: bourrache, onagre, argan, karité, avocat, olive..
                                                </span>
                                            </p>
                                            <p>
                                                <span style="font-weight: 400;">
                                                    Certaines huiles sont très fines et légères, c’est pourquoi on va les retrouver dans la 
                                                    composition des huiles dites “sèches” qui pénètrent plus vite dans la peau tout en y laissant 
                                                    un film satiné et agréable. C’est le cas du jojoba, tournesol, macadamia, sésame, noisette..
                                                </span>
                                            </p>
                                            <p>&nbsp;</p>
                                            <h2><b>Sous quelles formes peut-on les retrouver?</b></h2>
                                            <ul  style="list-style-type: disc;">
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        En liquide pour le corps, le visage ou les cheveux
                                                    </span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        En capsules à avaler pour nourrir la peau de l’intérieur
                                                    </span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        En lait et huile de douche où elles sont associées à des agents nettoyant
                                                    <span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        En huile solaire grâce à la présence de polymères gélifiants et à l’ajout 
                                                        de filtres solaires offrant une protection aussi élevée que les laits, 
                                                        crèmes ou gels solaires classiques
                                                    <span>
                                                </li>
                                            </ul>
                                            <p>&nbsp;</p>
                                            <h2>
                                                <b>Pour quels usages? </b>
                                            </h2>
                                            <ul style="list-style-type: disc;">
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        Pour la peau très sèche
                                                    </span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        Pour les cheveux fragilisés, secs et ternes
                                                    </span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        Pour les ongles cassants et striés
                                                    <span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        Pour aider à cicatriser uniquement quand la plaie est bien refermée
                                                    <span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        Pour prévenir l’apparition de vergetures
                                                    <span>
                                                </li>
                                                <li style="font-weight: 400;">
                                                    <span style="font-weight: 400;">
                                                        Pour le plaisir et la détente qu’offre un bon massage
                                                    <span>
                                                </li>
                                            </ul>
